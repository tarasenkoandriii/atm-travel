// Rotate: pick the first template not equal to lastId (keeps two-in-a-row from happening per country).
export function nextTemplate(lastId: string | null | undefined): TourTemplate {
  if (!lastId) return TEMPLATES[Math.floor(Math.random() * TEMPLATES.length)];
  const idx = TEMPLATES.findIndex((t) => t.id === lastId);
  return TEMPLATES[(idx + 1 + Math.floor(Math.random() * (TEMPLATES.length - 1))) % TEMPLATES.length];
}

